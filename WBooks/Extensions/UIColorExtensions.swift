import Foundation
import UIKit

extension UIColor {
    static func backgroundLightBlue() -> UIColor {
        return UIColor(red:0.92, green:0.96, blue:0.98, alpha:1)
    }
    
    static func backgroundDarkBlue() -> UIColor {
        return UIColor(red:0, green:0.68, blue:0.93, alpha:0.5)
    }
}
